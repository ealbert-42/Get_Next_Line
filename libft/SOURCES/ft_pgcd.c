#include "libft.h"

int		ft_pgcd(int nb1, int nb2)
{
	int		k;
	int		i;

	i = 0;
	k = 2;
	if ((nb1 < 2 && nb1 > -1) || (nb2 < 2 && nb2 > -1))
		return (1);
	while (k <= nb1 || k <= nb2)
	{
		if (nb1 % k == 0 && nb2 % k == 0)
			i = k;
		k++;
	}
	return (i);
}
